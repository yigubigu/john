package com.zmap.zmap.hospitalize.model;


public class OutHospitalPersonModel {

	private String deptCode;
	private String deptName;
	private String hospitalCode;
	private String respDept;
	private String outDate;
	private float outPeopleCount;
	private String sickmanCode;
	
	public String getDeptCode() {
		return deptCode;
	}
	public void setDeptCode(String deptCode) {
		this.deptCode = deptCode;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getHospitalCode() {
		return hospitalCode;
	}
	public void setHospitalCode(String hospitalCode) {
		this.hospitalCode = hospitalCode;
	}
	public String getRespDept() {
		return respDept;
	}
	public void setRespDept(String respDept) {
		this.respDept = respDept;
	}
	public String getOutDate() {
		return outDate;
	}
	public void setOutDate(String outDate) {
		this.outDate = outDate;
	}
	public float getOutPeopleCount() {
		return outPeopleCount;
	}
	public void setOutPeopleCount(float outPeopleCount) {
		this.outPeopleCount = outPeopleCount;
	}
	public String getSickmanCode() {
		return sickmanCode;
	}
	public void setSickmanCode(String sickmanCode) {
		this.sickmanCode = sickmanCode;
	}
	
	
}
