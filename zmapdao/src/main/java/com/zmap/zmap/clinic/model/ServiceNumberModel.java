package com.zmap.zmap.clinic.model;

public class ServiceNumberModel {

	private int serviceCount;
	private String regTime;
	private String inDate;
	private String deptCode;
	private String doctorCode;
	private String hospitalCode;
	private String sickmanCode;
	private String patientCode;
	private String respDoctorCode;
	private String respDept;
	
	public int getServiceCount() {
		return serviceCount;
	}
	public void setServiceCount(int serviceCount) {
		this.serviceCount = serviceCount;
	}
	public String getRegTime() {
		return regTime;
	}
	public void setRegTime(String regTime) {
		this.regTime = regTime;
	}
	public String getInDate() {
		return inDate;
	}
	public void setInDate(String inDate) {
		this.inDate = inDate;
	}
	public String getDeptCode() {
		return deptCode;
	}
	public void setDeptCode(String deptCode) {
		this.deptCode = deptCode;
	}
	public String getDoctorCode() {
		return doctorCode;
	}
	public void setDoctorCode(String doctorCode) {
		this.doctorCode = doctorCode;
	}
	public String getHospitalCode() {
		return hospitalCode;
	}
	public void setHospitalCode(String hospitalCode) {
		this.hospitalCode = hospitalCode;
	}
	public String getSickmanCode() {
		return sickmanCode;
	}
	public void setSickmanCode(String sickmanCode) {
		this.sickmanCode = sickmanCode;
	}
	public String getPatientCode() {
		return patientCode;
	}
	public void setPatientCode(String patientCode) {
		this.patientCode = patientCode;
	}
	public String getRespDoctorCode() {
		return respDoctorCode;
	}
	public void setRespDoctorCode(String respDoctorCode) {
		this.respDoctorCode = respDoctorCode;
	}
	public String getRespDept() {
		return respDept;
	}
	public void setRespDept(String respDept) {
		this.respDept = respDept;
	}
	
	
}
